def parse_input():
    return map(lambda l: map(lambda e: True if e=='#' else False, l) , inp.split('\n'))


def get_is_on(state, i, j):
    if (i==0 and j ==0) or (i==0 and j ==99) or (i==99 and j ==0)  or (i==99 and j ==99) :
        return True 
    if i<0 or j<0 or i>99 or j>99:
        return False
    return state[i][j]

def get_near_on(state, i, j):
    n = []

    n.append(get_is_on(state,i-1, j-1))
    n.append(get_is_on(state,i-1, j) )
    n.append(get_is_on(state,i-1, j+1) )
    n.append(get_is_on(state,i, j-1) )
    n.append(get_is_on(state,i, j+1) )
    n.append(get_is_on(state,i+1, j-1))
    n.append(get_is_on(state,i+1, j) )
    n.append(get_is_on(state,i+1, j+1))


    return reduce(lambda acc, x: acc+(1 if x else 0), n)


def next_state(state):
    ns = [[False] * 100 for i in range(100)]
    for i in range(100):
        for j in range(100):
            if (i==0 and j ==0) or (i==0 and j ==99) or (i==99 and j ==0)  or (i==99 and j ==99) :
                ns[i][j] = True
                continue
            no = get_near_on(state, i, j)
            if state[i][j]:
                if no in (2,3):
                    ns[i][j] = True
            else:
                if no == 3:
                    ns[i][j] = True

    return ns


